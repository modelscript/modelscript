/**
 * FMU Harness — C source generator.
 *
 * Generates a standalone C program that:
 *   1. Extracts an FMU (.fmu = ZIP) archive to a temp directory
 *   2. dlopen()s the shared library matching the current platform
 *   3. Reads JSON-RPC commands from stdin
 *   4. Calls FMI 2.0 C API functions
 *   5. Writes JSON-RPC responses to stdout
 *
 * Protocol (JSON-RPC over stdin/stdout, one JSON object per line):
 *   → {"method":"initialize","params":{"startTime":0,"stopTime":10,"stepSize":0.01},"id":1}
 *   ← {"result":"ok","id":1}
 *   → {"method":"doStep","params":{"currentTime":0,"stepSize":0.01},"id":2}
 *   ← {"result":"ok","id":2}
 *   → {"method":"getOutputs","id":3}
 *   ← {"result":{"x":1.0,"y":2.0},"id":3}
 *   → {"method":"setInputs","params":{"values":{"u":3.0}},"id":4}
 *   ← {"result":"ok","id":4}
 *   → {"method":"getState","id":5}
 *   ← {"result":{"stateId":1},"id":5}
 *   → {"method":"setState","params":{"stateId":1},"id":6}
 *   ← {"result":"ok","id":6}
 *   → {"method":"freeState","params":{"stateId":1},"id":7}
 *   ← {"result":"ok","id":7}
 *   → {"method":"terminate","id":8}
 *   ← {"result":"ok","id":8}
 */

/**
 * Generate the C source code for the FMU harness binary.
 *
 * This harness is platform-independent (Linux/macOS/Windows) and uses
 * the system's dynamic linker (dlopen/LoadLibrary) to load FMU shared libraries.
 */
export function generateFmuHarnessC(): string {
  return `/* Auto-generated by ModelScript — FMU harness binary */
/* Loads an FMU shared library and serves FMI 2.0 calls via JSON-RPC on stdin/stdout */

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <math.h>

#ifdef _WIN32
  #include <windows.h>
  #define DLOPEN(path) LoadLibraryA(path)
  #define DLSYM(handle, name) (void*)GetProcAddress((HMODULE)(handle), name)
  #define DLCLOSE(handle) FreeLibrary((HMODULE)(handle))
  #define PATH_SEP "\\\\"
#else
  #include <dlfcn.h>
  #include <sys/stat.h>
  #define DLOPEN(path) dlopen(path, RTLD_NOW)
  #define DLSYM(handle, name) dlsym(handle, name)
  #define DLCLOSE(handle) dlclose(handle)
  #define PATH_SEP "/"
#endif

/* ── FMI 2.0 type definitions ── */
typedef unsigned int fmi2ValueReference;
typedef double fmi2Real;
typedef int fmi2Integer;
typedef int fmi2Boolean;
typedef const char* fmi2String;
typedef void* fmi2Component;
typedef void* fmi2FMUstate;
typedef int fmi2Status;
typedef int fmi2Type;

#define fmi2OK 0
#define fmi2Warning 1
#define fmi2Discard 2
#define fmi2Error 3
#define fmi2Fatal 4
#define fmi2CoSimulation 1

/* FMI 2.0 callback types */
typedef void (*fmi2LoggerTF)(void*, fmi2String, fmi2Status, fmi2String, fmi2String, ...);
typedef void* (*fmi2AllocateMemoryTF)(size_t, size_t);
typedef void (*fmi2FreeMemoryTF)(void*);
typedef void (*fmi2StepFinishedTF)(void*, fmi2Status);

typedef struct {
  fmi2LoggerTF logger;
  fmi2AllocateMemoryTF allocateMemory;
  fmi2FreeMemoryTF freeMemory;
  fmi2StepFinishedTF stepFinished;
  void* componentEnvironment;
} fmi2CallbackFunctions;

/* ── FMI 2.0 function pointer types ── */
typedef fmi2Component (*fmi2InstantiateTF)(fmi2String, fmi2Type, fmi2String, fmi2String,
                                           const fmi2CallbackFunctions*, fmi2Boolean, fmi2Boolean);
typedef fmi2Status (*fmi2SetupExperimentTF)(fmi2Component, fmi2Boolean, fmi2Real, fmi2Real, fmi2Boolean, fmi2Real);
typedef fmi2Status (*fmi2EnterInitializationModeTF)(fmi2Component);
typedef fmi2Status (*fmi2ExitInitializationModeTF)(fmi2Component);
typedef fmi2Status (*fmi2DoStepTF)(fmi2Component, fmi2Real, fmi2Real, fmi2Boolean);
typedef fmi2Status (*fmi2GetRealTF)(fmi2Component, const fmi2ValueReference*, size_t, fmi2Real*);
typedef fmi2Status (*fmi2SetRealTF)(fmi2Component, const fmi2ValueReference*, size_t, const fmi2Real*);
typedef fmi2Status (*fmi2GetIntegerTF)(fmi2Component, const fmi2ValueReference*, size_t, fmi2Integer*);
typedef fmi2Status (*fmi2SetIntegerTF)(fmi2Component, const fmi2ValueReference*, size_t, const fmi2Integer*);
typedef fmi2Status (*fmi2GetBooleanTF)(fmi2Component, const fmi2ValueReference*, size_t, fmi2Boolean*);
typedef fmi2Status (*fmi2SetBooleanTF)(fmi2Component, const fmi2ValueReference*, size_t, const fmi2Boolean*);
typedef fmi2Status (*fmi2GetStringTF)(fmi2Component, const fmi2ValueReference*, size_t, fmi2String*);
typedef fmi2Status (*fmi2SetStringTF)(fmi2Component, const fmi2ValueReference*, size_t, const fmi2String*);
typedef fmi2Status (*fmi2TerminateTF)(fmi2Component);
typedef void (*fmi2FreeInstanceTF)(fmi2Component);
typedef fmi2Status (*fmi2GetFMUstateTF)(fmi2Component, fmi2FMUstate*);
typedef fmi2Status (*fmi2SetFMUstateTF)(fmi2Component, fmi2FMUstate);
typedef fmi2Status (*fmi2FreeFMUstateTF)(fmi2Component, fmi2FMUstate*);

/* ── FMU handle ── */
typedef struct {
  void* lib;
  fmi2Component comp;
  char modelIdentifier[256];
  char guid[256];
  char extractDir[1024];

  /* Function pointers */
  fmi2InstantiateTF Instantiate;
  fmi2SetupExperimentTF SetupExperiment;
  fmi2EnterInitializationModeTF EnterInitializationMode;
  fmi2ExitInitializationModeTF ExitInitializationMode;
  fmi2DoStepTF DoStep;
  fmi2GetRealTF GetReal;
  fmi2SetRealTF SetReal;
  fmi2GetIntegerTF GetInteger;
  fmi2SetIntegerTF SetInteger;
  fmi2GetBooleanTF GetBoolean;
  fmi2SetBooleanTF SetBoolean;
  fmi2GetStringTF GetString;
  fmi2SetStringTF SetString;
  fmi2TerminateTF Terminate;
  fmi2FreeInstanceTF FreeInstance;
  fmi2GetFMUstateTF GetFMUstate;
  fmi2SetFMUstateTF SetFMUstate;
  fmi2FreeFMUstateTF FreeFMUstate;

  /* State management */
  fmi2FMUstate savedStates[64];
  int nextStateId;
} FMUHandle;

/* ── Simple JSON helpers (minimal, no external deps) ── */

static void json_respond_ok(int id) {
  printf("{\\"result\\":\\"ok\\",\\"id\\":%d}\\n", id);
  fflush(stdout);
}

static void json_respond_error(int id, const char* msg) {
  printf("{\\"error\\":{\\"code\\":-1,\\"message\\":\\"%s\\"},\\"id\\":%d}\\n", msg, id);
  fflush(stdout);
}

/* ── Dummy callbacks ── */
static void dummyLogger(void* e, fmi2String i, fmi2Status s, fmi2String c, fmi2String m, ...) {
  (void)e; (void)i; (void)s; (void)c; (void)m;
}

/* ── Extract FMU (system unzip) ── */
static int extract_fmu(const char* fmuPath, char* outDir, size_t outDirSize) {
#ifdef _WIN32
  snprintf(outDir, outDirSize, "%s\\\\fmu-harness-%d", getenv("TEMP") ? getenv("TEMP") : ".", (int)GetCurrentProcessId());
  _mkdir(outDir);
  char cmd[2048];
  snprintf(cmd, sizeof(cmd), "powershell -Command \\"Expand-Archive -Path '%s' -DestinationPath '%s' -Force\\"", fmuPath, outDir);
#else
  snprintf(outDir, outDirSize, "/tmp/fmu-harness-%d", (int)getpid());
  mkdir(outDir, 0755);
  char cmd[2048];
  snprintf(cmd, sizeof(cmd), "unzip -o -q '%s' -d '%s'", fmuPath, outDir);
#endif
  return system(cmd);
}

/* ── Load FMI 2.0 function from shared library ── */
#define LOAD_FN(handle, name) \\
  handle->name = (fmi2##name##TF)DLSYM(handle->lib, "fmi2" #name); \\
  if (!handle->name) { fprintf(stderr, "Failed to load fmi2" #name "\\n"); }

static int load_fmu(FMUHandle* handle, const char* fmuPath) {
  /* Extract FMU */
  if (extract_fmu(fmuPath, handle->extractDir, sizeof(handle->extractDir)) != 0) {
    fprintf(stderr, "Failed to extract FMU\\n");
    return -1;
  }

  /* Parse modelDescription.xml for modelIdentifier and guid */
  char mdPath[1280];
  snprintf(mdPath, sizeof(mdPath), "%s%smodelDescription.xml", handle->extractDir, PATH_SEP);
  FILE* md = fopen(mdPath, "r");
  if (!md) {
    fprintf(stderr, "Cannot open modelDescription.xml\\n");
    return -1;
  }

  /* Simple XML attribute extraction (no full parser needed) */
  char buf[4096];
  handle->modelIdentifier[0] = '\\0';
  handle->guid[0] = '\\0';
  while (fgets(buf, sizeof(buf), md)) {
    char* p;
    if (handle->modelIdentifier[0] == '\\0' && (p = strstr(buf, "modelIdentifier=\\""))) {
      p += strlen("modelIdentifier=\\"");
      char* end = strchr(p, '"');
      if (end) { size_t len = (size_t)(end - p); if (len >= sizeof(handle->modelIdentifier)) len = sizeof(handle->modelIdentifier) - 1; memcpy(handle->modelIdentifier, p, len); handle->modelIdentifier[len] = '\\0'; }
    }
    if (handle->guid[0] == '\\0' && (p = strstr(buf, "guid=\\""))) {
      p += strlen("guid=\\"");
      char* end = strchr(p, '"');
      if (end) { size_t len = (size_t)(end - p); if (len >= sizeof(handle->guid)) len = sizeof(handle->guid) - 1; memcpy(handle->guid, p, len); handle->guid[len] = '\\0'; }
    }
  }
  fclose(md);

  if (handle->modelIdentifier[0] == '\\0') {
    fprintf(stderr, "Could not find modelIdentifier in XML\\n");
    return -1;
  }

  /* Load shared library */
  char libPath[1536];
#if defined(_WIN32)
  snprintf(libPath, sizeof(libPath), "%s%sbinaries%swin64%s%s.dll",
           handle->extractDir, PATH_SEP, PATH_SEP, PATH_SEP, handle->modelIdentifier);
#elif defined(__APPLE__)
  snprintf(libPath, sizeof(libPath), "%s%sbinaries%sdarwin64%s%s.dylib",
           handle->extractDir, PATH_SEP, PATH_SEP, PATH_SEP, handle->modelIdentifier);
#else
  snprintf(libPath, sizeof(libPath), "%s%sbinaries%slinux64%s%s.so",
           handle->extractDir, PATH_SEP, PATH_SEP, PATH_SEP, handle->modelIdentifier);
#endif

  handle->lib = DLOPEN(libPath);
  if (!handle->lib) {
#ifndef _WIN32
    fprintf(stderr, "dlopen failed: %s\\n", dlerror());
#else
    fprintf(stderr, "LoadLibrary failed: %lu\\n", GetLastError());
#endif
    return -1;
  }

  /* Load all function pointers */
  LOAD_FN(handle, Instantiate);
  LOAD_FN(handle, SetupExperiment);
  LOAD_FN(handle, EnterInitializationMode);
  LOAD_FN(handle, ExitInitializationMode);
  LOAD_FN(handle, DoStep);
  LOAD_FN(handle, GetReal);
  LOAD_FN(handle, SetReal);
  LOAD_FN(handle, GetInteger);
  LOAD_FN(handle, SetInteger);
  LOAD_FN(handle, GetBoolean);
  LOAD_FN(handle, SetBoolean);
  LOAD_FN(handle, GetString);
  LOAD_FN(handle, SetString);
  LOAD_FN(handle, Terminate);
  LOAD_FN(handle, FreeInstance);
  LOAD_FN(handle, GetFMUstate);
  LOAD_FN(handle, SetFMUstate);
  LOAD_FN(handle, FreeFMUstate);

  handle->nextStateId = 0;
  memset(handle->savedStates, 0, sizeof(handle->savedStates));

  return 0;
}

/* ── Simple JSON number/string parser ── */
static double parse_json_number(const char* json, const char* key) {
  char search[128];
  snprintf(search, sizeof(search), "\\"%s\\":", key);
  const char* p = strstr(json, search);
  if (!p) return 0.0;
  p += strlen(search);
  while (*p == ' ') p++;
  return atof(p);
}

static int parse_json_int(const char* json, const char* key) {
  return (int)parse_json_number(json, key);
}

static const char* parse_json_string(const char* json, const char* key, char* out, size_t outSize) {
  char search[128];
  snprintf(search, sizeof(search), "\\"%s\\":\\"", key);
  const char* p = strstr(json, search);
  if (!p) { out[0] = '\\0'; return out; }
  p += strlen(search);
  size_t i = 0;
  while (*p && *p != '"' && i < outSize - 1) {
    out[i++] = *p++;
  }
  out[i] = '\\0';
  return out;
}

/* ── Main command loop ── */
int main(int argc, char* argv[]) {
  if (argc < 2) {
    fprintf(stderr, "Usage: fmu-harness <path-to-fmu>\\n");
    return 1;
  }

  FMUHandle handle;
  memset(&handle, 0, sizeof(handle));

  if (load_fmu(&handle, argv[1]) != 0) {
    fprintf(stderr, "Failed to load FMU: %s\\n", argv[1]);
    return 1;
  }

  /* Read JSON-RPC commands line by line */
  char line[65536];
  while (fgets(line, sizeof(line), stdin)) {
    /* Parse method */
    char method[64];
    parse_json_string(line, "method", method, sizeof(method));
    int id = parse_json_int(line, "id");

    if (strcmp(method, "initialize") == 0) {
      double startTime = parse_json_number(line, "startTime");
      double stopTime = parse_json_number(line, "stopTime");
      double stepSize = parse_json_number(line, "stepSize");

      if (!handle.Instantiate) { json_respond_error(id, "Instantiate not loaded"); continue; }

      fmi2CallbackFunctions callbacks = { dummyLogger, calloc, free, NULL, NULL };
      handle.comp = handle.Instantiate(
        handle.modelIdentifier, fmi2CoSimulation, handle.guid,
        handle.extractDir, &callbacks, 0, 0
      );

      if (!handle.comp) { json_respond_error(id, "fmi2Instantiate failed"); continue; }

      fmi2Status s;
      if (handle.SetupExperiment) {
        s = handle.SetupExperiment(handle.comp, 0, stepSize, startTime, 1, stopTime);
        if (s > fmi2Warning) { json_respond_error(id, "SetupExperiment failed"); continue; }
      }
      if (handle.EnterInitializationMode) {
        s = handle.EnterInitializationMode(handle.comp);
        if (s > fmi2Warning) { json_respond_error(id, "EnterInitializationMode failed"); continue; }
      }
      if (handle.ExitInitializationMode) {
        s = handle.ExitInitializationMode(handle.comp);
        if (s > fmi2Warning) { json_respond_error(id, "ExitInitializationMode failed"); continue; }
      }

      json_respond_ok(id);
    }
    else if (strcmp(method, "doStep") == 0) {
      double currentTime = parse_json_number(line, "currentTime");
      double stepSize = parse_json_number(line, "stepSize");

      if (!handle.DoStep || !handle.comp) { json_respond_error(id, "Not initialized"); continue; }

      fmi2Status s = handle.DoStep(handle.comp, currentTime, stepSize, 1);
      if (s > fmi2Warning) { json_respond_error(id, "doStep failed"); continue; }

      json_respond_ok(id);
    }
    else if (strcmp(method, "getOutputs") == 0) {
      /* Read all FMU variables and return as JSON object */
      /* For simplicity we return all Real variables; the TS side filters */
      if (!handle.comp) { json_respond_error(id, "Not initialized"); continue; }

      printf("{\\"result\\":{");

      /* Read variables by VR — the caller must know the VR mapping */
      /* We'll read VR 0..99 as a practical upper bound */
      int first = 1;
      for (unsigned int vr = 0; vr < 100; vr++) {
        fmi2Real val;
        fmi2ValueReference vrArr[1] = { vr };
        if (handle.GetReal && handle.GetReal(handle.comp, vrArr, 1, &val) == fmi2OK) {
          if (!first) printf(",");
          printf("\\"%u\\":%g", vr, val);
          first = 0;
        }
      }

      printf("},\\"id\\":%d}\\n", id);
      fflush(stdout);
    }
    else if (strcmp(method, "setInputs") == 0) {
      /* Parse "values" object: {"values":{"0":1.0,"1":2.0}} */
      if (!handle.comp) { json_respond_error(id, "Not initialized"); continue; }

      const char* valuesStart = strstr(line, "\\"values\\":{");
      if (valuesStart) {
        valuesStart += strlen("\\"values\\":{");
        /* Parse key-value pairs */
        const char* p = valuesStart;
        while (*p && *p != '}') {
          if (*p == '"') {
            p++;
            unsigned int vr = (unsigned int)atoi(p);
            while (*p && *p != '"') p++;
            if (*p == '"') p++;
            if (*p == ':') p++;
            double val = atof(p);
            while (*p && *p != ',' && *p != '}') p++;

            fmi2ValueReference vrArr[1] = { vr };
            fmi2Real valArr[1] = { val };
            if (handle.SetReal) handle.SetReal(handle.comp, vrArr, 1, valArr);
          }
          if (*p == ',') p++;
          else if (*p != '"' && *p != '}') p++;
        }
      }

      json_respond_ok(id);
    }
    else if (strcmp(method, "getState") == 0) {
      if (!handle.comp || !handle.GetFMUstate) {
        json_respond_error(id, "getState not supported");
        continue;
      }

      fmi2FMUstate state = NULL;
      fmi2Status s = handle.GetFMUstate(handle.comp, &state);
      if (s > fmi2Warning || !state) {
        json_respond_error(id, "getFMUstate failed");
        continue;
      }

      int stateId = handle.nextStateId++;
      if (stateId < 64) {
        handle.savedStates[stateId] = state;
      }

      printf("{\\"result\\":{\\"stateId\\":%d},\\"id\\":%d}\\n", stateId, id);
      fflush(stdout);
    }
    else if (strcmp(method, "setState") == 0) {
      int stateId = parse_json_int(line, "stateId");
      if (!handle.comp || !handle.SetFMUstate || stateId < 0 || stateId >= 64 || !handle.savedStates[stateId]) {
        json_respond_error(id, "setState failed");
        continue;
      }

      fmi2Status s = handle.SetFMUstate(handle.comp, handle.savedStates[stateId]);
      if (s > fmi2Warning) { json_respond_error(id, "setFMUstate failed"); continue; }

      json_respond_ok(id);
    }
    else if (strcmp(method, "freeState") == 0) {
      int stateId = parse_json_int(line, "stateId");
      if (!handle.comp || !handle.FreeFMUstate || stateId < 0 || stateId >= 64 || !handle.savedStates[stateId]) {
        json_respond_error(id, "freeState failed");
        continue;
      }

      handle.FreeFMUstate(handle.comp, &handle.savedStates[stateId]);
      handle.savedStates[stateId] = NULL;
      json_respond_ok(id);
    }
    else if (strcmp(method, "terminate") == 0) {
      if (handle.comp) {
        if (handle.Terminate) handle.Terminate(handle.comp);
        if (handle.FreeInstance) handle.FreeInstance(handle.comp);
        handle.comp = NULL;
      }
      if (handle.lib) {
        DLCLOSE(handle.lib);
        handle.lib = NULL;
      }

      json_respond_ok(id);
      break; /* Exit after terminate */
    }
    else {
      json_respond_error(id, "Unknown method");
    }
  }

  return 0;
}
`;
}

/**
 * Generate a CMakeLists.txt for building the FMU harness binary.
 */
export function generateFmuHarnessCMake(): string {
  return `# Auto-generated by ModelScript — FMU harness build
cmake_minimum_required(VERSION 3.10)
project(fmu-harness C)

set(CMAKE_C_STANDARD 99)

add_executable(fmu-harness fmu-harness.c)

if(UNIX)
  target_link_libraries(fmu-harness dl)
endif()

install(TARGETS fmu-harness DESTINATION bin)
`;
}
